    <h1>À propos de SkillSwipe</h1>

    <p><strong>SkillSwipe</strong> est une plateforme web imaginée et développée dans le cadre d’un projet étudiant.
        Notre objectif est simple : faciliter le matching entre étudiants et recruteurs grâce à une approche intuitive
        inspirée des applications de swipe modernes.</p>

    <h2>Pourquoi SkillSwipe ?</h2>
    <p>Parce que la recherche de stage ou d’alternance ne devrait pas être un calvaire. Nous avons voulu rendre le
        processus plus fluide, plus rapide, et surtout plus intelligent grâce à un algorithme de matching vectoriel basé
        sur les compétences.</p>

    <h2>Fonctionnalités clés</h2>
    <ul>
        <li>Matching intelligent basé sur des vecteurs de profil</li>
        <li>Swipe visuel et rapide (like / dislike)</li>
        <li>Possibilité pour les recruteurs de swiper les étudiants également</li>
        <li>Suivi des likes, des matchs, et des interactions</li>
    </ul>

    <h2>Équipe projet</h2>
    <p>SkillSwipe est un projet porté par une équipe étudiante motivée, passionnée par le développement web, l’UX, et la
        recherche de solutions concrètes pour les étudiants.</p>

